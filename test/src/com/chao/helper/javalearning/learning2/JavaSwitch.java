public class JavaSwitch {
    public static void main(String[] args) {
        int a = 3;
        switch (a){
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("1");
                break;
            case 3:
                System.out.println("3");
                break;
            default:
                System.out.println("都不是");
                break;
        }
    }
}
